    private void btnBuscarAutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarAutoActionPerformed
        // TODO add your handling code here:
        String catego;
        String marca;
        String modelo;
        char tipo;
        String enunciadoSQL = "";

        catego = txtCatego.getText().toUpperCase().trim();
        marca = txtMarca.getText().toUpperCase().trim();
        modelo = txtModelo.getText().toUpperCase().trim();
        if (rbtnTipoA.isSelected()) {
            tipo = 'A';
        } else if (rbtnTipoS.isSelected()) {
            tipo = 'B';
        } else {
            tipo = 'X'; //ambos
        }
        if (catego.length() > 0) {
            enunciadoSQL = "select * from autos where categoria like '%" + catego + "%'";
        } else if (marca.length() > 0) {
            enunciadoSQL = "select * from autos where marca like '%" + marca + "%'";
        } else if (modelo.length() > 0) {
            enunciadoSQL = "select * from autos where modelo like '%" + modelo + "%'";
        }

        con.consultar(enunciadoSQL);
        int nr = con.getNumRegistros();
        if (con.irPrimero()) {
            for (int i = 0; i < nr; i++) {
                tblDatos.setValueAt(con.obtenerCampo("placa"), i, 0);
                tblDatos.setValueAt(con.obtenerCampo("categoria"), i, 1);
                tblDatos.setValueAt(con.obtenerCampo("marca"), i, 2);
                tblDatos.setValueAt(con.obtenerCampo("modelo"), i, 3);
                tblDatos.setValueAt(con.obtenerCampo("color"), i, 4);
                tblDatos.setValueAt(con.obtenerCampo("renta"), i, 5);
                con.irSiguiente();
            }
        }

    }//GEN-LAST:event_btnBuscarAutoActionPerformed

