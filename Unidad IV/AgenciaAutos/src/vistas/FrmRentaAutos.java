    private void btnBuscarClieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarClieActionPerformed
        // TODO add your handling code here:
        String licencia;
        String enunciadoSQL;
        
        licencia = txtLicencia.getText().toUpperCase().trim();
        enunciadoSQL = "select * from cliente where licencia='"+licencia+"'";
        con.consultar(enunciadoSQL);
        if(con.irPrimero()){
            txtIdCliente.setText(con.obtenerCampo("idcliente"));
            txtNomCliente.setText(con.obtenerCampo("nombre"));
            cmbTipo.setSelectedItem(con.obtenerCampo("tipo"));
            
        }else {    
            JOptionPane.showMessageDialog(this, "No existe ese registro");
        }

    }//GEN-LAST:event_btnBuscarClieActionPerformed

    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        String enunciadoSQL;
        String placa;
        int idCliente;
        String fechaRenta;
        String fechaDevolucion;
        double monto;
        String formato;
        SimpleDateFormat sdf1;
        Date fechaR;
        Date fechaD;
        int fila;
        
        fila = tblDatos.getSelectedRow();
        placa = tblDatos.getValueAt(fila, 0).toString();
        idCliente= Integer.parseInt(txtIdCliente.getText());
        
        formato = txtFechaR.getDateFormatString();
        sdf1 = new SimpleDateFormat(formato);
        fechaR = txtFechaR.getDate();
        fechaD = txtFechaD.getDate();
        
        fechaRenta = sdf1.format(fechaR);
        fechaDevolucion = sdf1.format(fechaD);
        monto = Double.parseDouble(txtMonto.getText());
        enunciadoSQL = "insert into rentados (placa, idcliente,fecharenta,"
                + "fechadevolucion,fechaentrega,monto) values ('"+placa+"',"
                + "'"+idCliente+"', '"+fechaRenta+"', '"+fechaDevolucion+"',"
                + "'', '"+monto+"')";
        
    }//GEN-LAST:event_btnAceptarActionPerformed

