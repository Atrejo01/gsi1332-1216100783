    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        int clave;
        String nombre;
        String direccion;
        String carrera;
        String genero;
        int edad;
        String estado;
        String enunciadoSQL;

        clave = Integer.parseInt(txtClave.getText());
        nombre = txtNombre.getText().toUpperCase().trim();
        direccion = txtDireccion.getText().toUpperCase().trim();
        carrera = cmbCarrera.getSelectedItem().toString();
        genero = cmbGenero.getSelectedItem().toString();
        edad = Integer.parseInt(txtEdad.getText());
        estado = cmbEstadoCivil.getSelectedItem().toString();

        enunciadoSQL = "UPDATE tutorados SET nombre='" + nombre + "',"
                + "direccion='" + direccion + "',carrera = '" + carrera + "', "
                + "genero='" + genero + "', edad = '" + edad + "', estadocivil= '" + estado + "'"
                + "WHERE clave = " + clave;

        con.actualizar(enunciadoSQL);
    }//GEN-LAST:event_btnModificarActionPerformed

