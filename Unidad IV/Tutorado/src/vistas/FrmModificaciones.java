    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        int clave;
        String enunciadoSQL;
        try {
            clave = Integer.parseInt(txtClave.getText());
            enunciadoSQL = "SELECT * FROM tutorados WHERE clave =" + clave;

            con.consultar(enunciadoSQL);
            int nr = con.getNumRegistros();
            if (con.irPrimero() && nr==1) {
                txtClave.setText(con.obtenerCampo("clave"));
                txtNombre.setText(con.obtenerCampo("nombre"));
                txtDireccion.setText(con.obtenerCampo("direccion"));
                cmbCarrera.setSelectedItem(con.obtenerCampo("carrera"));
                cmbGenero.setSelectedItem(con.obtenerCampo("genero"));
                txtEdad.setText(con.obtenerCampo("edad"));
                cmbEstadoCivil.setSelectedItem(con.obtenerCampo("estadocivil"));
            } else {
                JOptionPane.showMessageDialog(this, "EL REGISTRO NO EXISTE");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Ingrese una clave");
            
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        // TODO add your handling code here:
        int clave = Integer.parseInt(txtClave.getText());
        int op = JOptionPane.showConfirmDialog(this, "Desea eliminar este registro?");
        if (op == JOptionPane.OK_OPTION) {
            String enunciadSQL = "DELETE FROM tutorados WHERE clave=" + clave;
            con.actualizar(enunciadSQL);
        }
        txtClave.setText("");
        txtNombre.setText("");
        txtDireccion.setText("");
        cmbCarrera.setSelectedIndex(0);
        cmbGenero.setSelectedIndex(0);
        txtEdad.setText("");
        cmbEstadoCivil.setSelectedIndex(0);

    }//GEN-LAST:event_btnBorrarActionPerformed

