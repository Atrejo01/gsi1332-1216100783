    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:

        String carrera;
        String genero;
        String estado;
        String enunciadoSQL = "";

        
            carrera = cmbCarrera.getSelectedItem().toString();
            genero = cmbGenero.getSelectedItem().toString();
            estado = cmbEstadoCivil.getSelectedItem().toString();

            if (cmbCarrera.getSelectedIndex() > 0) {
                enunciadoSQL = "SELECT * FROM tutorados WHERE carrera LIKE '%" + carrera + "%'";
            } else if (cmbGenero.getSelectedIndex() > 0) {
                enunciadoSQL = "SELECT * FROM tutorados WHERE genero LIKE '%" + genero + "%'";
            } else if (cmbEstadoCivil.getSelectedIndex() > 0) {
                enunciadoSQL = "SELECT * FROM tutorados WHERE estadocivil LIKE '%" + estado + "%'";
            }

            con.consultar(enunciadoSQL);
            int num = con.getNumRegistros();
            if (num > 0) {
                if (con.irPrimero()) {
                    for (int i = 0; i < num; i++) {
                        tblDatos.setValueAt(con.obtenerCampo("clave"), i, 0);
                        tblDatos.setValueAt(con.obtenerCampo("nombre"), i, 1);
                        tblDatos.setValueAt(con.obtenerCampo("direccion"), i, 2);
                        tblDatos.setValueAt(con.obtenerCampo("carrera"), i, 3);
                        tblDatos.setValueAt(con.obtenerCampo("genero"), i, 4);
                        tblDatos.setValueAt(con.obtenerCampo("edad"), i, 5);
                        tblDatos.setValueAt(con.obtenerCampo("estadocivil"), i, 6);
                        con.irSiguiente();
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "No existen registros con esos datos");
            }
        
        cmbCarrera.setSelectedIndex(0);
        cmbGenero.setSelectedIndex(0);
        cmbEstadoCivil.setSelectedIndex(0);

    }//GEN-LAST:event_btnBuscarActionPerformed

