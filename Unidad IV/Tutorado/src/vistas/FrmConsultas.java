    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        String enunciadoSQL;
        int pos;
        int clave;
        int op;

        pos = tblDatos.getSelectedRow();
        if (pos != -1) {
            op = JOptionPane.showConfirmDialog(this, "Eliminar registro?");
            if (op == JOptionPane.OK_OPTION) {
                clave = Integer.parseInt(tblDatos.getValueAt(pos, 0).toString());
                enunciadoSQL = "Delete from tutorados where clave=" + clave;
                con.actualizar(enunciadoSQL);
                limpiarTabla();

            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona un registro para eliminar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

